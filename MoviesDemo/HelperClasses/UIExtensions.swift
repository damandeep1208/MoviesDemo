extension UIImage {
    //Global constants and variables are always computed lazily
    static let starSelected: UIImage? = UIImage(named: "starSelected")
    static let starGrey: UIImage? = UIImage(named: "starGrey")
    static let star: UIImage? = UIImage(named: "star")
    static let starWhite: UIImage? = UIImage(named: "starWhite")
    static let bookmarkBlack: UIImage? = UIImage(named: "bookmarkBlack")
    static let bookmarkWhite: UIImage? = UIImage(named: "bookmarkWhite")
    static let bookmarkBlackSelected: UIImage? = UIImage(named: "bookmarkBlackSelected")
    static let bookmarkWhiteSelected: UIImage? = UIImage(named: "bookmarkWhiteSelected")
    static let Backgroundbg: UIImage? = UIImage(named: "Backgroundbg")

}

